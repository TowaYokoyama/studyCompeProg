H, B = map(int, input().split())

# 穴
holes = []
for _ in range(H):
    x, y = map(int, input().split())
    holes.append((x, y))

# ボール
balls = []
for _ in range(B):
    p, q = map(int, input().split())
    balls.append((p, q))
#落ちたボールの数
ans = 0
#グリッドを傾けた操作履歴
S = input()
#辞書ベクトル
dirs = {
    'R': (1, 0),
    'L': (-1, 0),
    'U': (0, 1),
    'D': (0, -1)
}
