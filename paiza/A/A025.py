from collections import defaultdict

N, S, T = map(int, input().split())

A = []
B = []
for _ in range(N):
    a, b = map(int, input().split())
    A.append(a)
    B.append(b)

# dp[w] = 現在の体重が w になる通り数
dp = defaultdict(int)
dp[S] = 1  # 初期状態

for i in range(N):
    next_dp = defaultdict(int)
    ai = A[i]
    bi = B[i]

    for w, cnt in dp.items():
        # ダイエットする
        w1 = w - ai
        if w1 <= T:
            next_dp[w1] += cnt

        # ダイエットしない
        w2 = w + bi
        if w2 <= T:
            next_dp[w2] += cnt

    dp = next_dp  # 次の日へ

# 最終日に残ったすべての通り数を合計
answer = sum(dp.values())
print(answer)
