T_R,T_G,T_B = map(int,input().split())
N = int(input())
cp = [list(map(int, input().split())) for _ in range(N)]

found = False 

for bit in range(1, 1<<N):
    selected = []
    for i in range(N):
        if bit & (1 << i):
            selected.append(cp[i])
            
    k = len(selected)
    r = sum(p[0] for p in selected) // k
    g = sum(p[1] for p in selected) // k
    b = sum(p[2] for p in selected) // k
    
    if r == T_R and g == T_G and b == T_B:
        found = True
        break
    
print("Yes" if found else "No") 