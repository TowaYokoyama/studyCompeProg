N,M = map(int,input().split())

a = [int(input()) for _ in range(M)]
scores = []
for _ in range(N):
    score = 100 
    for j in range(M):
        h = int(input())
        diff = abs(a[j] - h)

        if diff <= 5:
            pass
        elif diff <= 10:
            score -= 1
        elif diff <= 20:
            score -= 2
        elif diff <= 30:
            score -= 3
        else:
            score -= 5

        if score < 0:
            score = 0

    scores.append(score)

