from collections import defaultdict

N = int(input())
A = list(map(int, input().split()))
W = list(map(int, input().split()))

box = defaultdict(list)

for i in range(N):
    box[A[i]].append(W[i])

cost = 0
for weights in box.values():
    weights.sort(reverse=True)
    cost += sum(weights[1:])  # 一番重いの以外を動かす

print(cost)
