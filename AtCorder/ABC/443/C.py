N, T = map(int, input().split())
A = list(map(int, input().split())) if N > 0 else []

open_time = 0  # 次にchokutterを開いている時刻（開いていなければ未来の再開時刻）
ans = 0

for a in A:
    if open_time < a:              # aの時点で開いている（= open_time から a まで見てた）
        ans += a - open_time
        open_time = a + 100        # 閉じたので100秒後に再開
    else:
        # aの時点で開いていない（まだ再開時刻に達していない）
        pass

# 終業まで、開いている時間が残っていれば足す
if open_time < T:
    ans += T - open_time

print(ans)
