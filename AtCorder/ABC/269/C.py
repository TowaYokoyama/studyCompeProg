N = int(input())

# ① N の 2進数で「1 が立っている桁」を集める
pos = []
i = 0
tmp = N
while tmp > 0:
    if tmp % 2 == 1:
        pos.append(i)
    tmp //= 2
    i += 1

# ② その桁たちの部分集合を bit 全探索
res = []
k = len(pos)

for mask in range(1 << k):
    x = 0
    for j in range(k):
        if (mask >> j) & 1:
            x += 1 << pos[j]
    res.append(x)

# ③ 昇順で出力
res.sort()
for v in res:
    print(v)


