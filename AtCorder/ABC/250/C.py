N,Q = map(int, input().split())
A = [i + 1 for i in range(N)] # 現在のボールの並び（位置→値）
pos = [i for i in range(N)]  # pos[x-1]: xの位置


for _ in range(Q):
  #操作対象列
  x = int(input())
  idx = pos[x -1] #xの位置
  
  if idx == N - 1:
    #右端の場合、左隣と入れ替え
    swap_idx = idx -1
  else:
    swap_idx = idx + 1
    
  #Aをswap
  A[idx],A[swap_idx] = A[swap_idx],A[idx]
  #posを更新
  pos[A[idx] - 1] = idx
  pos[A[swap_idx] - 1] = swap_idx  
     
