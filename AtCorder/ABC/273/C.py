N = int(input())
A = list(map(int, input().split()))

# ① 種類だけ取り出してソート
vals = sorted(set(A))
M = len(vals)

# ② 値 → インデックス（順位）に変換
pos = {v: i for i, v in enumerate(vals)}

# ③ K ごとのカウント箱
ans = [0] * N

# ④ 各 A[i] を見て、どの K に入るか数える
for x in A:
    k = M - 1 - pos[x]   # 自分より大きい「種類数」
    ans[k] += 1

# ⑤ 出力（K=0,1,2,...）
for x in ans:
    print(x)
