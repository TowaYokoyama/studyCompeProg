H, W = map(int, input().split())
c = [input() for _ in range(H)]

# 黒ピクセルの行・列の最小・最大インデックスを求める
top = min(i for i in range(H) if '#' in c[i])
bottom = max(i for i in range(H) if '#' in c[i])
left = min(j for i in range(H) for j in range(W) if c[i][j] == '#')
right = max(j for i in range(H) for j in range(W) if c[i][j] == '#')

# その範囲だけ出力
for i in range(top, bottom + 1):
    print(c[i][left:right + 1])