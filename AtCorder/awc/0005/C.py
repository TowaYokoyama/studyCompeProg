import sys
input = sys.stdin.readline

N, K = map(int, input().split())
A = list(map(int, input().split()))

# 左からの下限
left = [0] * N
left[0] = A[0]
for i in range(1, N):
    # 前の値から K 以内に収めるための下限
    left[i] = max(A[i], left[i-1] - K)

# 右からの下限
right = [0] * N
right[N-1] = A[N-1]
for i in range(N-2, -1, -1):
    right[i] = max(A[i], right[i+1] - K)

# 最終的な B を決めて、追加分を合計
ans = 0
for i in range(N):
    B_i = max(left[i], right[i])
    ans += B_i - A[i]

print(ans)
