import heapq

N, M = map(int, input().split())

heap = []

for _ in range(N):
    F, D = map(int, input().split())
    if F > 0:
        heapq.heappush(heap, (-F, D))

total = 0

for _ in range(M):
    if not heap:
        break
    
    neg_val, D = heapq.heappop(heap)
    val = -neg_val
    
    if val <= 0:
        break
    
    total += val
    
    next_val = val - D
    if next_val > 0:
        heapq.heappush(heap, (-next_val, D))

print(total)
